/**
 * 금지어·위험어를 안전한 대체어로 치환
 * @param {string} text
 * @returns {string}
 */
function autoFix(text) {
  let fixed = text;
  const esc = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  // 1) 문맥 친화적 치환(FIX_MAP)
  for (const [from, to] of FIX_MAP) {
    fixed = fixed.replace(new RegExp(esc(from), 'gi'), to);
  }
  // 2) 보장 스윕 — FIX_MAP이 못 지운 잔여 금지어를 안전어로 마저 치환(자동발행 보장)
  for (const [from, to] of FORBIDDEN_FALLBACK) {
    fixed = fixed.replace(new RegExp(esc(from), 'gi'), to);
  }
  return fixed;
}

