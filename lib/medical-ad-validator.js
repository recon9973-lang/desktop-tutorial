/**
 * 금지어·위험어를 안전한 대체어로 치환
 * @param {string} text
 * @returns {string}
 */
function autoFix(text) {
  let fixed = text;
  for (const [from, to] of FIX_MAP) {
    // 대소문자 구분 없이 치환
    const re = new RegExp(from.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
    fixed = fixed.replace(re, to);
  }
  return fixed;
}

